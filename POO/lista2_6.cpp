//O encapsulamento traz vários benefícios para a programação, tais como: 
//Segurança: ao esconder os dados sensíveis ou críticos de um objeto, você evita que eles sejam acessados ou alterados indevidamente por outras partes do código,
//o que poderia causar erros ou inconsistências.
//private: significa que o elemento só pode ser acessado ou modificado pelo próprio objeto que o declara, e não por nenhum outro objeto ou classe.
//public: significa que o elemento pode ser acessado ou modificado por qualquer objeto ou classe, sem restrições.
//protected: significa que o elemento pode ser acessado ou modificado pelo próprio objeto que o declara, ou por qualquer objeto ou classe que seja
//do mesmo pacote ou que seja uma subclasse do objeto que o declara.
